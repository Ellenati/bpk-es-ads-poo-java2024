package exercicio02;

public class VariaveisTipos {
    public static void main(String[] args) {
        int inteiro = 10;
        double decimal = 5.5;
        boolean verdadeiro = true;
        char caractere = 'A';

        System.out.println("Valor inteiro: " + inteiro);
        System.out.println("Valor decimal: " + decimal);
        System.out.println("Valor booleano: " + verdadeiro);
        System.out.println("Valor caractere: " + caractere);
    }
}
